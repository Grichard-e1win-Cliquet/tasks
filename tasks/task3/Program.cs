Console.Clear();
Console.WriteLine("Enter a number of day of the week: ");
int x = Convert.ToInt32(Console.ReadLine());

if (x == 1) Console.WriteLine("Today is Monday");
if (x == 2) Console.WriteLine("Today is Tuesday");
if (x == 3) Console.WriteLine("Today is Wednesday");
if (x == 4) Console.WriteLine("Today is Thursday");
if (x == 5) Console.WriteLine("Today is Friday");
if (x == 6) Console.WriteLine("Today is Saturday");
if (x == 7) Console.WriteLine("Today is Sunday");
